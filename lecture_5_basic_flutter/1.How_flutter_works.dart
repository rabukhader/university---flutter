// Flutter is a framework from Google used to build apps.
// We write code once, and Flutter draws the interface on the screen.
//
// Main idea
// Flutter has: Framework, Engine, Rendering, Widget tree
//
// 1. Framework
// This is the part we use directly.
//
// It includes: widgets, gestures, animations, material design
// This is the layer we write in as developers.
//
// 2. Engine
//
// The engine is the part that works behind the scenes.
//
// It handles: rendering, drawing, text layout, communication with platform
//
// The engine is like the machine that takes our Flutter code and turns it into something visible.
//
// 3. GPU
//
// GPU helps draw graphics very fast.
//
// The GPU is the part that helps display shapes, colors, text, and animations smoothly on the screen.
//
// 4. Rendering
//
// Rendering means drawing the UI on the screen.
//
// When we write a button in code, Flutter renders it, meaning it actually draws it on the device.
//
// 5. Trees in Flutter
// Flutter uses trees.
//
// Widget Tree: Shows all widgets inside other widgets.
//
// Example:
//
// MaterialApp
// Scaffold
// AppBar
// Body
// Column
// Text
// Button
//
// In Flutter, every screen is built from widgets inside widgets.
// So the app looks like a tree.
//
