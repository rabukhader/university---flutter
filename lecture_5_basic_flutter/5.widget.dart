// A widget is the basic building block in Flutter.
//
// In Flutter, everything is a widget.
//
// Examples:
//
// text
// button
// image
// row
// column
// screen
// Important idea
//
// A widget can contain other widgets.
//
// Example
// Center(
//   child: Text("Hello"),
// )
//
// Center is a widget
// Text is also a widget
//
// Another example
// MaterialApp(
//   home: Scaffold(
//     appBar: AppBar(
//       title: Text("My App"),
//     ),
//     body: Center(
//       child: Text("Welcome"),
//     ),
//   ),
