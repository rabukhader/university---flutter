// What is a function?
// A function is a block of code that does a specific task.
//
// Example
// void greet() {
//   print("Hello");
// }
//
// void main() {
//   greet();
// }

// Explanation
// greet() is the function name
// void means it does not return anything
// inside main(), we call the function

// Function with parameters
// void greetStudent(String name) {
//   print("Hello, $name");
// }
//
// void main() {
//   greetStudent("Ahmad");
//   greetStudent("Sara");
// }
// Output
// Hello, Ahmad
// Hello, Sara

// Function that returns a value
// int add(int a, int b) {
//   return a + b;
// }
//
// void main() {
//   int result = add(5, 3);
//   print(result);
// }

