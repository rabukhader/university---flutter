// Anonymous function means a function without a name.
// It is often used inside another function.
//
// Example
// void main() {
//   var names = ["Ahmad", "Sara", "Lina"];
//
//   names.forEach((name) {
//     print(name);
//   });
// }
//
// Here:
// (name) { print(name); } is an anonymous function
// Another example
// void main() {
// var numbers = [1, 2, 3];
//
// numbers.forEach((number) {
// print(number * 2);
// });
// }
// Output
// 2
// 4
// 6