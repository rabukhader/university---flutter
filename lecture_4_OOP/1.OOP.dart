// What is OOP?
//
// OOP means we organize code like real life.

// Instead of writing everything randomly, we create:
// Class = blueprint
// Object = real thing made from that blueprint

// Real-life example
// If I say Car:
// color
// model
// speed

// This is the idea of a class.
// A real car like: Toyota, white, 120 km/h
// This is an object.
//
//
// A class is like a design or template.
// An object is the real thing we create from that design.

//2. Why OOP is important
//
// OOP is useful because it makes code:
// 1. Modularity : We divide code into small organized parts. Instead of putting everything in one file, we give each thing its own place.
// 2. Reusability: We write code once and use it many times. If I create a Student class once, I can create 100 students from it.
// 3. Flexibility : We can change one part without breaking everything.
// 4.Maintainability : The code becomes easier to read, fix, and improve.
//
// OOP helps us write cleaner code, easier code, and smarter code.