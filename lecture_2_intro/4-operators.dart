// Operators are symbols used to perform actions on values.
//
// 1. Arithmetic operators: Used for math.
//
// void main() {
//   int a = 10;
//   int b = 5;
//
//   print(a + b); // 15
//   print(a - b); // 5
//   print(a * b); // 50
//   print(a / b); // 2.0
//   print(a % b); // 0
// }

// Explanation
// + add
// - subtract
// * multiply
// / divide
// % remainder

// 2. Assignment operators: Used to assign values.
//
// void main() {
//   int x = 10;
//   x += 5;
//   print(x); // 15
// }
//
// Other examples:
//
// void main() {
//   int x = 20;
//
//   x -= 5;
//   print(x); // 15
//
//   x *= 2;
//   print(x); // 30
// }

// 3. Comparison operators: Used to compare values.
//
// void main() {
//   int a = 10;
//   int b = 5;
//
//   print(a > b);  // true
//   print(a < b);  // false
//   print(a == b); // false
//   print(a != b); // true
// }

// Common comparison operators
// > greater than
// < less than
// == equal
// != not equal
// >= greater than or equal
// <= less than or equal

// 4. Logical operators: Used with true/false values.
//
// void main() {
//   int age = 20;
//   bool hasId = true;
//
//   print(age >= 18 && hasId); // true
// }
// Explanation
// && means AND
// || means OR
// ! means NOT
//
// Example:
//
// void main() {
//   bool isSunny = true;
//   bool hasUmbrella = false;
//
//   print(isSunny || hasUmbrella); // true
//   print(!hasUmbrella); // true
// }
