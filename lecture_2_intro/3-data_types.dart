// Data Types
// What is a data type?
// A data type tells Dart what kind of value is stored.
// Common data types in Dart
// 1. String: Used for text.
//
// void main() {
//   String name = "Rasheed";
//   print(name);
// }

// 2. int: Used for whole numbers.
//
// void main() {
//   int age = 24;
//   print(age);
// }

// 3. double: Used for decimal numbers.
//
// void main() {
//   double price = 12.5;
//   print(price);
// }

// 4. bool: Used for true or false values.
//
// void main() {
//   bool isStudent = true;
//   print(isStudent);
// }

// 5. List: Used to store multiple values in one variable.
//
// void main() {
//   List<String> names = ["Ahmad", "Sara", "Lina"];
//   print(names);
// }
//

// Access an item:
//
// void main() {
//   List<String> names = ["Ahmad", "Sara", "Lina"];
//   print(names[0]);
// }
//
// Output:
//
// Ahmad

// 6. Map
//
// Used to store key-value data.
