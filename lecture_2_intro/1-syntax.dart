// Syntax means the rules of writing code correctly.
// In Flutter, we write code using Dart.
// Basic Dart syntax example
//
// void main() {
//   print("Hello World");
// }
// Explanation
// void means this function does not return a value
// main() is the starting point of the program
// {} contains the code block
// print() displays output in the console
// ; ends the statement

// Important Flutter syntax rules
// 1. Every statement usually ends with ;
// int age = 20;
// print(age);
// 2. Code blocks use { }
// if (age > 18) {
// print("Adult");
// }
// 3. Strings use quotes
// String name = "Rasheed";
// String city = 'Hebron';

